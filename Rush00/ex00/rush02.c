void	ft_putchar(char c);

void	rush(int x, int y)
{
	int i;
	int j;

	i = 0;
	j = 0;
	while (j < y && y > 0 && x > 0)
	{
		while (i < x)
		{
			if ((j == 0 && i == 0) || (j == 0 && i == x - 1 && x != 1))
				ft_putchar('A');
			else if ((j == y - 1 && i == 0 && y != 1) ||
					(j == y - 1 && i == x - 1))
				ft_putchar('C');
			else if (((j == 0 || j == y - 1) && i > 0 && i < x - 1) ||
					((i == 0 || i == x - 1) && j > 0 && j < y - 1))
				ft_putchar('B');
			else
				ft_putchar(' ');
			i++;
		}
		ft_putchar(10);
		i = 0;
		j++;
	}
}
