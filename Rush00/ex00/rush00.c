void	ft_putchar(char c);

void	rush(int x, int y)
{
	int a;
	int b;

	a = 0;
	b = 0;
	while (a < y && x > 0 && y > 0)
	{
		while (b < x)
		{
			if ((b == 0 && (a == 0 || a == y - 1)) ||
					(b == x - 1 && (a == 0 || a == y - 1)))
				ft_putchar('o');
			else if ((a > 0 && a < y - 1) && (b == 0 || b == x - 1))
				ft_putchar('|');
			else if ((b > 0 && b < x - 1) && (a == 0 || a == y - 1))
				ft_putchar('-');
			else
				ft_putchar(' ');
			b++;
		}
		b = 0;
		ft_putchar('\n');
		a++;
	}
}
